#include <stdio.h>

int main() {

    int num = 6;
    printf("%d\n", num);
    // % is used to tell c that we will print out something special
    // and the letter after % tells c about the data type of the value to be printed.
    
    printf("Hello\"World\n");
    // we use \" to use " in between " "

    return 0;

}

// printf() is a function which prints out the values given to it.

// %d for integers, %c for a single character, %s for an array, %f and %lf for float and double
